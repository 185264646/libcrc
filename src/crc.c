static uint16_t rev_16b(uint16_t reg)
{
	unsigned char hi, lo;
	hi = reg >> 8;
	lo = reg & 0xFF;
	hi = rev_table[hi];
	lo = rev_table[lo];
	reg = (lo << 8) | hi;
	return reg;
}


uint16_t crc16(const unsigned char *data, size_t len, uint16_t poly, uint16_t init, uint16_t xorout, bool refin, bool refout)
{
    uint16_t reg = init;
    while(len--)
    {
		reg ^= (refin ? rev_table[*data++] : *data++) << 8;
        for(int i = 0; i < 8; i++)
        {
            bool hi = reg & 0x8000;
            reg <<= 1;
            if(hi) // extract the top bit
            {
                reg ^= poly;
            }
        }
    }
    reg ^= xorout;
    return refout ? rev_16b(reg) : reg;
}

uint16_t crc16_xmodem(const unsigned char *data, size_t len)
{
    uint16_t reg = 0;
    while(len--) reg = (reg << 8) ^ crc16_xmodem_table[(reg >> 8) ^ *data++];
    return reg;
}

uint16_t crc16_ibm(const unsigned char *data, size_t len)
{
	uint16_t reg = 0;
    while(len--) reg = (reg >> 8) ^ crc16_ibm_table[(reg & 0xFF) ^ *data++];
	return reg;
}
